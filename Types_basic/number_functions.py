#Importamos el módulo math 
import math
 
print(round(1.3)) #La función round redondea el número al entero más cercano 
print(round(1.7))  
print(round(1.5)) #Si el número es exactamente equidistante entre dos enteros, redondea al entero par más cercano 
print(abs(-20)) #La función abs devuelve el valor absoluto de un número (su distancia al 0) 
print(abs(20))  
 
#Funciones del modulo math 
 
print(math.ceil(1.3)) #La función ceil redondea el número al entero más cercano mayor o igual que el número 
print(math.floor(1.7)) #La función floor redondea el número al entero más cercano menor o igual que el número 
print(math.isnan(1.7)) #La función isnan devuelve True si el número es NaN (Not a Number) 
#print(math.isnan("23")) Si el argumento no es un número, devuelve False 
print(math.pow(2,3)) #La función pow devuelve el primer número elevado al segundo 
print(math.sqrt(9)) #La función sqrt devuelve la raíz cuadrada del número